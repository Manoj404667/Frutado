function ui_trans(name) {
    var cur_map;

    if (typeof(ui_lang_map[CurrentLangID()] == 'object')) {
        cur_map = ui_lang_map[CurrentLangID()];
    } else {
        cur_map = ui_lang_map["1031"];
    }

    if (typeof(cur_map[name]) == 'string') {
        return cur_map[name];
    } else if (typeof(ui_lang_map["1031"][name]) == 'string') {
        return ui_lang_map["1031"][name];
    } else {
        return '[' + name + ']';
    }
}

